/**
 
CSS BOX MODEL
-------------

Starting from the center and moving outward, here is the breakdown:

Content: This is where your text, images, or videos live. Its dimensions are controlled by width and height.

Padding: The transparent space inside the element. It creates breathing room between the content and the border.

Border: The line that wraps around the padding and content. You can style its thickness, color, and texture.

Margin: The transparent space outside the border. It’s used to push other elements away and create distance between boxes.

 */